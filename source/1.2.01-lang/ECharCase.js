MO.ECharCase = new function ECharCase(){
   var o = this;
   ///@Attribute String 字符大写
   o.Upper = 'U';
   ///@Attribute String 字符小写
   o.Lower = 'L';
   ///@Attribute String 字符按源格式写
   o.Word  = 'W';
   return o;
}
