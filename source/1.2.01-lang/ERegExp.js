MO.ERegExp = new function ERegExp(){
   var o = this;
   // 整数
   o.I  = /^-?[1-9]\d*|0$/;
   // 正整数
   o.PI = /^[1-9]\d*$/;
   // 负整数
   o.NI = /^-[1-9]\d*$/;
   // 浮点数
   o.F  = /^-?([1-9]\d*\.\d*|0\.\d*[1-9]\d*|0?\.0+|0)$/;
   // 正浮点数
   o.PF = /^[1-9]\d*\.\d*|0\.\d*[1-9]\d*$/;
   // 负浮点数
   o.NF = /^-([1-9]\d*\.\d*|0\.\d*[1-9]\d*)$/;
   // 网络地址
   o.U  = /[1-9]{1}[0-9]/;
   // 邮件地址
   o.E  = /^\w{1,}[@]{1}[a-zA-Z]{1,}[.]{1}[a-zA-Z]{1,}$/;
   return o;
}
