//==========================================================
MO.TLocker = function TLocker(o){
   if(!o){o = this;}
   //..........................................................
   // @attribute
   o._lock = false;
   //..........................................................
   // @method
   o.enter = TLocker_enter;
   o.leave = TLocker_leave;
   return o;

   //==========================================================
   // <T>锁定处理。</T>
   //
   // @method
   //==========================================================
   function TLocker_enter(){
      this._lock = true;
   }

   //==========================================================
   // <T>解锁处理。</T>
   //
   // @method
   //==========================================================
   function TLocker_leave(){
      this._lock = false;
   }
}
