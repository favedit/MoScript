//==========================================================
// <T>名称接口。</T>
//
// @face
// @author maocy
// @history 150411
//==========================================================
function MName(o){
   o = RClass.inherits(this, o);
   //..........................................................
   // @attribute
   o._name   = null;
   //..........................................................
   // @method
   o.name    = MName_name;
   o.setName = MName_setName;
   return o;
}

//==========================================================
// <T>获得名称。</T>
//
// @return String 名称
//==========================================================
function MName_name(){
   return this._name;
}

//==========================================================
// <T>设置名称。</T>
//
// @param name:String 名称
//==========================================================
function MName_setName(name){
   this._name = name;
}
