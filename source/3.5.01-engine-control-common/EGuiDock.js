MO.EGuiDock = new function EGuiDock(){
   var o = this;
   // @attribute
   o.None        = 'none';
   o.LeftTop     = 'left.top';
   o.Left        = 'left';
   o.LeftBottom  = 'left.bottom';
   o.RightTop    = 'right.top';
   o.Right       = 'right';
   o.RightBottom = 'right.bottom';
   o.Center      = 'center';
   o.Fill        = 'fill';
   return o;
}
