function ECursorFace(){
   var o = this;
   // Attribute
   o.Default   = 'default';
   o.NorthWest = 'NW';
   o.SouthWest = 'SW';
   o.SouthEast = 'SE';
   o.NorthEast = 'NE';
   o.West      = 'W';
   o.South     = 'S';
   o.East      = 'E';
   o.North     = 'N';
   return o;
}
var ECursor = new ECursorFace();
