var ENumber = new function ENumber(){
   var o = this;
   // @member 整数
   o.Integer              = 'I';
   // @member 正整数
   o.PositiveInteger      = 'PI';
   // @member 负整数
   o.NegativeInteger      = 'NI';
   // @member 浮点数
   o.Float                = 'F';
   // @member 正浮点数
   o.PositiveFloat        = 'PF';
   // @member 负浮点数
   o.NegativeFloat        = 'NF';
   return o;
}
