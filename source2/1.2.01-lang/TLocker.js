with(MO){
   //==========================================================
   // <T>锁定器。</T>
   //
   // @tool
   // @author maocy
   // @version 150105
   //==========================================================
   MO.TLocker = function TLocker(){
      var o = this;
      //..........................................................
      // @attribute
      o._lock = false;
      //..........................................................
      // @method
      o.enter = TLocker_enter;
      o.leave = TLocker_leave;
      return o;
   }

   //==========================================================
   // <T>锁定处理。</T>
   //
   // @method
   //==========================================================
   MO.TLocker_enter = function TLocker_enter(){
      this._lock = true;
   }

   //==========================================================
   // <T>解锁处理。</T>
   //
   // @method
   //==========================================================
   MO.TLocker_leave = function TLocker_leave(){
      this._lock = false;
   }
}
